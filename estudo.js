let horaDigitada = 12;

if (horaDigitada === 1) {
  console.log("Boa noite");
}

// IF ELSE------------------------------------------------------------------------------------------------------------
console.log("if");
if (horaDigitada < 6) {
  console.log("Boa noite");
} else {
  console.log("Bom dia");
}

// IF ELSE IF--------------------------------------------------------------------------------------------------------
horaDigitada = 1;
if (horaDigitada > 4 && horaDigitada <= 12) {
  console.log("Bom dia");
} else if (horaDigitada > 12 && horaDigitada <= 18) {
  console.log("Boa tarde");
} else {
  console.log("Boa noite");
}

// ------------------------------------------------------------------------------------------------------------------------------
// ESTRUTURA REPETICAO---------------------------------------------------------------------------------------------------------
// ------------------------------------------------------------------------------------------------------------------------------

// WHILE--------------------------------------------------------------------------------------------------------
console.log("While");
horaDigitada = 1;
while (horaDigitada < 18) {
  console.log("Bom dia");
  horaDigitada++;
}

// FOR----------------------------------------------------------------------------------------------------------
horaDigitada = 1;
console.log("For");
for (horaDigitada = 1; horaDigitada < 18; horaDigitada++) {
  console.log("Bom dia");
}

// DO WHILE-----------------------------------------------------------------------------------------------------
console.log("Do while");
horaDigitada = 1;
do {
  console.log("Bom dia");
  horaDigitada++;
} while (horaDigitada < 18);

